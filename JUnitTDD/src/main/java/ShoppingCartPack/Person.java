package ShoppingCartPack;

public class Person {
    private int age;
    private String name;

    public Person(int age, String name) {
        this.age = age;
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public String getName() {
            return name;
    }

    public boolean isValid(){
            if(!name.isEmpty() && age<200 && age >0)return true;
        return false;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public void setName(String name) {
        this.name = name;
    }
}
